class Trainer:
    def __init__(self, options):
        self.opt = options
        self.log_path = os.path.join(self.opt.log_dir, self.opt.model_name)

        # MODELS
        # Load network architecture
        ''' Setting Models, initialization and optimization '''
        self.models = {}
        self.parameters_to_train = []

